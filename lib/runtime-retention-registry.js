function buildDefaultRuntimeRoots(options = {}) {
  const homeDir = options.homeDir || os.homedir();
  const appName = options.appName || 'COTW Trail Guide';
  const userDataPath = options.userDataPath || path.join(homeDir, 'Library', 'Application Support', appName);
  const openclawHome = options.openclawHome || path.join(homeDir, '.openclaw-cotw');
  const workspacePath = options.workspacePath || path.join(userDataPath, 'workspace');
  const pluginsPath = options.pluginsPath || path.join(options.repoRoot || path.resolve(__dirname, '..'), 'bundled-plugins');
  const agentId = options.agentId || 'trail-guide';

  return {
    homeDir,
    userDataPath,
    openclawHome,
    openclawLogs: path.join(openclawHome, 'logs'),
    sessionsDir: path.join(openclawHome, 'agents', agentId, 'sessions'),
    workspacePath,
    workspaceSpine: path.join(workspacePath, 'spine'),
    workspaceEvolution: path.join(workspacePath, 'evolution'),
    workspaceImports: path.join(workspacePath, 'imports'),
    pluginsPath,
    continuityData: path.join(pluginsPath, 'openclaw-plugin-continuity', 'data'),
    harnessRefinerData: path.join(pluginsPath, 'openclaw-plugin-harness-refiner', 'data'),
    metabolismData: path.join(pluginsPath, 'openclaw-plugin-metabolism', 'data'),
    cognitiveDynamicsData: path.join(pluginsPath, 'openclaw-plugin-cognitive-dynamics', 'data'),
  };
}

